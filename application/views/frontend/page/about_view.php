                <p>TRƯỜNG THCS PHÚ LÃM</p>

                <p>LỄ KỈ NIỆM NGÀY NHÀ GIÁO VIỆT NAM 20-11-2015</p>

                <p>Phần I:</p>

                <p>7h tập trung học sinh.<br />
                    7h 15 phút tập trung đội nghi thức.<br />
                    Văn nghệ học sinh.<br />
                    8h bắt đầu làm việc: Bây giờ đã đến giờ làm việc vậy kính mời các vị đại biểu cùng toàn thể các thầy cô giáo về khu vực lễ đài để buổi lễ được bắt đầu.<br />
                    Kính thưa các quý vị đại biểu, các thầy cô giáo cùng toàn thể các em học sinh yêu quý. Hôm nay chúng ta có mặt ở đây để tổ chức lễ kỷ niệm 33 năm Ngày nhà giáo việt nam (20/11/1982 - 20/11/2015). Lời đầu tiên cho phép tôi gửi tới quý vị đại biểu, các thầy cô giáo cùng toàn thể các em học sinh lời kính chúc sức khỏe, hạnh phúc và thành công. Chúc buổi lễ kỷ niệm của chúng ta thành công tốt đẹp!<br />
                    Sau đây chúng ta sẽ chuyển sang phần nội dung chính của buổi lễ.</p>

                <p>Phần II:</p>

                <p>1. Mời đội nghi thức đứng dậy.</p>

                <p>Đội nghi thức vào vị trí - Các em học sinh đứng lên.</p>

                <p>2. Nhiệt liệt chào mừng các vị đại biểu khách quý, các thầy cô giáo về dự Lễ kỉ niệm ngày nhà giáo Việt Nam!</p>

                <p>( Trống chào mừng )</p>

                <p>3. Tôi xin trân trọng kinh mời các vị đại biểu khách, các thầy giáo, cô giáo và các em học sinh đứng lên làm lễ chào cờ.</p>

                <p>-&nbsp; “Nghiêm!”&nbsp; “ Chào cờ - Chào”</p>

                <p>- “ Quốc ca! ”</p>

                <p>- Xin kính mời các vị đại biểu khách, các thầy giáo, cô giáo an tọa. mời các em học sinh ngồi xuống!</p>

                <p>4. Đội nghi thức về vị trí.</p>

                <p>5. Kính thưa các vị đại biểu khách quý!</p>

                <p>&nbsp;&nbsp;&nbsp; Kính thưa các thầy giáo, cô giáo!</p>

                <p>&nbsp;&nbsp;&nbsp; Kính thưa toàn thể vác em Học sinh yêu mến!</p>

                <p>Từ bao đời nay nhân dân ta đã có truyền thống “ tôn sư trọng đạo”: “ Muốn sang thì bắc cầu Kiều / Muốn con hay chữ thì yêu lấy thầy”. Nghề dạy học luôn được tôn vinh và trọng dụng. Dạy học mãi là một nghề tâm huyết, cao quý, đúng như nhà sư phạm nổi tiếng người Nga Cô Men Xki đã nói:” Dưới ánh mặt trời không nghề nào cao quý hơn nghề dạy học”.</p>

                <p>Thực hiện theo lời dạy của bác Hồ kính yêu:</p>

                <p>Vì lợi ích mười năm (thì phải) trồng cây</p>

                <p>Vì lợi ích trăm năm (thì phải) trồng người</p>

                <p>Toàn Đảng toàn dân ta luôn ý thức chăm lo cho sự nghiệp giáo dục và đào tạo. Mùa thu năm 1982, Đảng và nhà nước đã ra quyết định lấy ngày 20-11 là ngày “Nhà giáo Việt Nam” làm nức lòng giáo giới và nhân dân cả nước.</p>

                <p>Ngày 20/11 là dịp để các thế hệ học trò bày tỏ lòng biết ơn sâu sắc với những người thầy, để mọi ngành, mọi nghề và toàn xã hội chia sẻ niềm vui, tri ân với những người đã góp bao công sức và tâm huyết cho sự nghiệp trồng người cao cả, góp phần xây dựng đất nước phồn vinh, hạnh phúc, và ngày 20-11 đã đi vào lịch sử như một ngày hội lớn của toàn dân tộc.</p>

                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Kính thưa các vị đại biểu khách quý!</p>

                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Kính thưa các thầy giáo, cô giáo!</p>

                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;Kính thưa toàn thể vác em Học sinh yêu mến!</p>

                <p>Là người dân Việt Nam chúng ta không thể nào quên truyền thống “ Uống nước nhớ nguồn – Tôn sư trọng đạo” mà ông cha ta đã truyền dạy từ bao đời nay. “ Không thầy đố mày làm nên…”. Chính thầy giáo, cô giáo là những người cần mẫn chèo lái con thuyền đưa từng lứa học sinh cập bến bờ tri thức. Thầy cô giữ một vị trí quan trọng đó là “ Kỹ sư tâm hồn – Vườn ươm thế hệ tương lai!”.</p>
